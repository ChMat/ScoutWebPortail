<h2>Le Portail</h2>
<h3>Court historique</h3>
<p>Le Scout Web Portail est un projet open-source cr&eacute;&eacute; &agrave; 
  l'origine pour la 5e Unit&eacute; scoute Namur-Meuse &agrave; Andenne. D'&eacute;volution 
  en &eacute;volution, ce portail s'est enrichi de nombreux outils et, suite &agrave; 
  la demande d'autres unit&eacute;s, l'id&eacute;e est venue de diffuser le portail 
  &agrave; tous les groupes scouts int&eacute;ress&eacute;s par ce genre d'outil.</p>
<h3>Une alternative id&eacute;ale pour les groupes scouts</h3>
<p>Internet est un outil de communication tr&egrave;s puissant. De nombreux groupes 
  scouts l'ont compris et ont r&eacute;cemment cr&eacute;&eacute; leur site web. 
  Malheureusement, la maintenance de ces espaces d&eacute;pend bien souvent d'une 
  seule personne, ce qui rend les mises &agrave; jour longues et fastidieuses. 
  Le Scout Web Portail offre une gestion ais&eacute;e du contenu par l'ensemble 
  des animateurs du groupe scout. L'administration du site est r&eacute;alis&eacute;e 
  &agrave; partir d'une interface web et est en grande partie automatis&eacute;e.</p>
<p>L'avantage essentiel du Scout Web Portail est son orientation sp&eacute;cifique 
  vers les groupes scouts. Sa structure a &eacute;t&eacute; con&ccedil;ue pour 
  accueillir un groupe scout compos&eacute; de une ou plusieurs unit&eacute;s, 
  elle-m&ecirc;mes compos&eacute;es d'une ou plusieurs sections. Elle permet de 
  g&eacute;rer la visibilit&eacute; de chacune des sections sur le web ainsi que 
  la vie quotidienne de chacune d'entre elles. </p>
<p>En effet, un outil complet permet de g&eacute;rer les donn&eacute;es des membres 
  des sections de mani&egrave;re centralis&eacute;e. Ainsi, l'ensemble des animateurs 
  a acc&egrave;s de mani&egrave;re directe aux donn&eacute;es &agrave; jour de 
  l'ensemble du groupe scout. Bien entendu, ces donn&eacute;es peuvent &ecirc;tre 
  export&eacute;es tr&egrave;s simplement vers des logiciels tels que Microsoft 
  Excel.</p>
<p>La gestion de ces donn&eacute;es &agrave; caract&egrave;re priv&eacute; se 
  fait en toute s&eacute;curit&eacute;. Seuls les animateurs ont acc&egrave;s 
  &agrave; ces derni&egrave;res.</p>
<p>Viens d&eacute;couvrir le <a href="http://www.scoutwebportail.org" target="_blank">Scout Web 
  Portail</a> !</p>
